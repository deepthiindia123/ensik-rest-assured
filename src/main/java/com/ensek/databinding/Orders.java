package com.ensek.databinding;

public class Orders {
    private String fuel;
    private String Id;
    private String id;
    private int quantity;
    private String time;

    public String getFuel() {
        return fuel;
    }

    public String getId() {
        return Id;
    }

    public String getid() {
        return id;
    }

    public int getQuantity() {
        return quantity;
    }

    public String getTime() {
        return time;
    }
}
